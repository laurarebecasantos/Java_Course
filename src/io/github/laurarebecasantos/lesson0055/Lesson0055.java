package io.github.laurarebecasantos.lesson0055;

class Lesson0055 {

	public static void main(String[] args) {
		
		int[] list = new int[3];
		list[0] = 10;
		list[1] = 20;
		list[2] = 30;
		
		for(int item : list) {
			System.out.println(item);
		}
		
		
		
		/* Lesson0055
		 * For Each
		 * 
		 * It is a specialized repetition structure.
		 * Printing list by for each.
		 * 
		 */
	}
}
