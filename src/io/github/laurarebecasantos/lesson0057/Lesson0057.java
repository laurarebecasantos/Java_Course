package io.github.laurarebecasantos.lesson0057;

class Lesson0057 {
	
	public static void main(String[] args) {
		
		int[][] values = new int[5][5];
		for(int x=0; x<values.length; x++) {
			for(int y=0; y<values.length; y++) {
				values[x][y]=9;
				System.out.println(values[x][y]+" ");
				System.out.println();
			}
			
		}
		
		
		/* Lesson0057
		 * Nested cycles
		 * 
		 * Manipulating values in the multidimensional matrix
		 * 
		 */
		
	}
	
	

}
